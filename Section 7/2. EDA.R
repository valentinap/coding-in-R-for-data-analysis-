# functions to explore the dataset

dim() # gives us the number of cases and the number of variables in a dataset
str() # the type of data, the number of cases and variables and the type of each
summary() # a summary of some elements, such as minimum, maximum, mean, median and quantile values of each variable
nrow() # the number of rows
ncol() # the number of columns
names() # names of variables
class() # the types of object
head() # the first instances of a dataset
tail() # the last instances of a dataset
mean() # the average
median() # the median 
quantile() # the quantiles

